#!/bin/bash

echo "=========================================="
echo "百度DNS解析测试 - 国内DNS服务器"
echo "=========================================="
echo

DOMAIN="baidu.com"

echo "1. 使用114.114.114.114解析:"
echo "----------------------------------------"
dig @114.114.114.114 +short $DOMAIN
echo

echo "2. 使用223.5.5.5解析:"
echo "----------------------------------------"
dig @223.5.5.5 +short $DOMAIN
echo

echo "3. 对比系统默认DNS:"
echo "----------------------------------------"
dig +short $DOMAIN
echo

echo "=========================================="
echo "详细解析信息对比:"
echo "=========================================="
echo

echo "4. 114.114.114.114 详细信息:"
echo "----------------------------------------"
dig @114.114.114.114 $DOMAIN A +noall +answer
echo

echo "5. 223.5.5.5 详细信息:"
echo "----------------------------------------"
dig @223.5.5.5 $DOMAIN A +noall +answer
echo

echo "6. 系统默认DNS 详细信息:"
echo "----------------------------------------"
dig $DOMAIN A +noall +answer
echo

echo "=========================================="
echo "测试完成"
echo "=========================================="